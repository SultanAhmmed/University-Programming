#include <bits/stdc++.h>

#define nn "\n"
#define ll long long

using namespace std;


void REIGN() {

  int test; cin >> test;

  while (test--) {

    int t;  cin >> t;
    for (int i = 1; i <= t; i++) {
      cout << i * 2 << ' ';
    }
    cout << nn;
  }
}

int main()
{
  REIGN();
  return 0;
}
